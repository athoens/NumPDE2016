function [x,w]=gaussTriangle(p)

% points for "Gauss integration" in the triangle (-1,-1), (1,-1), (-1,1)
%
% input:
% p - order of the numerical integration (1 <= n <= 5)
%
% output:
% x - 2xn-array of points, where n is the number of points
% w - 1xn-array of weights, where n is the number of points

if p == 1
    x = [-1/3; -1/3];
    w = 2;
elseif p == 2
    x = [[-2/3; -2/3],...
         [-2/3;  1/3],...
         [ 1/3; -2/3]];
    w = [2/3,...
         2/3,...
         2/3];
elseif p == 3
    x = [[-1/3; -1/3],...
         [-0.6; -0.6],...
         [-0.6;  0.2],...
         [ 0.2; -0.6]];
    w = [-1.125,...
          1.041666666666667,...
          1.041666666666667,...
          1.041666666666667];
elseif p == 4
    x = [[-0.108103018168070; -0.108103018168070],...
         [-0.108103018168070; -0.783793963663860],...
         [-0.783793963663860; -0.108103018168070],...
         [-0.816847572980458; -0.816847572980458],...
         [-0.816847572980458;  0.633695145960918],...
         [ 0.633695145960918; -0.816847572980458]];
    w = [0.446763179356022,...
         0.446763179356022,...
         0.446763179356022,...
         0.219903487310644,...
         0.219903487310644,...
         0.219903487310644];
elseif p == 5
    x = [[-0.333333333333333; -0.333333333333333],...
         [-0.059715871789770; -0.059715871789770],...
         [-0.059715871789770; -0.880568256420460],...
         [-0.880568256420460; -0.059715871789770],...
         [-0.797426985353088; -0.797426985353088],...
         [-0.797426985353088;  0.594853970706174],...
         [ 0.594853970706174; -0.797426985353088]];
    w = [0.450000000000000,...
         0.264788305577012,...
         0.264788305577012,...
         0.264788305577012,...
         0.251878361089654,...
         0.251878361089654,...
         0.251878361089654];
else
    fprintf('ERROR: numerical integration of order %d not available.\n',p);
end